package eu.neclab.iotplatform.iotbroker.storage.hsqldb;

import java.io.IOException;
import java.net.DatagramSocket;
import java.net.ServerSocket;
import java.util.Properties;

import org.apache.log4j.Logger;
import org.hsqldb.Server;
import org.hsqldb.persist.HsqlProperties;
import org.hsqldb.server.ServerAcl.AclFormatException;
import org.springframework.context.SmartLifecycle;

/**
 * Wrapper class for a hyperSqlDb Server.
 */
public class HyperSqlDbServer implements SmartLifecycle {

	private static Logger logger = Logger.getLogger(HyperSqlDbServer.class);

	private final HsqlProperties properties;
	private Server server;
	private boolean running = false;

	public HyperSqlDbServer(Properties props) {
		properties = new HsqlProperties(props);
	}

	@Override
	public boolean isRunning() {
		if (server != null) {
			server.checkRunning(running);
		}
		return running;
	}

	@Override
	public void start() {

		if (available(Integer.valueOf(System.getProperty("hsqldb.port")))) {

			if (server == null) {
				server = new Server();
				try {
					server.setProperties(properties);
					server.start();
					running = true;
				} catch (AclFormatException afe) {

					logger.debug("AclFormatException", afe);

				} catch (IOException e) {
					logger.debug("IOException", e);
				}
			}
		} else {

		}
	}

	@Override
	public void stop() {
		if (server != null) {
			server.stop();
			running = false;
		}
	}

	@Override
	public int getPhase() {
		return 0;
	}

	@Override
	public boolean isAutoStartup() {
		return true;
	}

	@Override
	public void stop(Runnable runnable) {
		stop();
		runnable.run();
	}

	public static boolean available(int port) {

		ServerSocket ss = null;
		DatagramSocket ds = null;
		try {
			ss = new ServerSocket(port);
			ss.setReuseAddress(true);
			ds = new DatagramSocket(port);
			ds.setReuseAddress(true);
			return true;
		} catch (IOException e) {
		} finally {
			if (ds != null) {
				ds.close();
			}

			if (ss != null) {
				try {
					ss.close();
				} catch (IOException e) {
					/* should not be thrown */
				}
			}
		}

		return false;
	}
}
